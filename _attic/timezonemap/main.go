// Generator for timezonemap.go.
// Fetches Firebird's TimeZones.h and emits both switch functions plus
// getTimezoneVersion.
//
// Usage:
//
//	go run ./_attic/timezonemap                          # fetch from upstream master
//	go run ./_attic/timezonemap -url <url>               # custom branch/tag
//	go run ./_attic/timezonemap -src /path/to/TimeZones.h # local file
//	go run ./_attic/timezonemap -out /path/to/timezonemap.go
